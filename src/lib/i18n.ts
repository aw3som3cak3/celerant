const DICT: Record<Locale, Dict> = { sv, en };

// translate('key', { n: 5 }) replaces {n} in the string.
export function translate(locale: Locale, key: string, params?: Record<string, string | number>): string {
  let s = DICT[locale][key] ?? DICT[DEFAULT_LOCALE][key] ?? key;
  if (params) for (const [k, v] of Object.entries(params)) s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
  return s;
}
